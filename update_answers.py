def update_json_file():
    try:
        # Открываем существующий файл с вопросами
        with open('questions.json', 'r', encoding='utf-8') as file:
            data = json.load(file)

        # Проверяем совпадение количества вопросов
        if len(data) != len(correct_answers):
            print(f"Внимание: В вашем файле {len(data)} вопросов, а ответов подготовлено {len(correct_answers)}.")

        # Записываем правильные ответы в каждый вопрос
        for index, item in enumerate(data):
            if index < len(correct_answers):
                answer = correct_answers[index]
                # Если правильный ответ один, записываем число, если несколько — массив чисел
                if len(answer) == 1:
                    item['correctIndex'] = answer[0]
                else:
                    item['correctIndex'] = answer

        # Сохраняем обновленный файл
        with open('questions.json', 'w', encoding='utf-8') as file:
            json.dump(data, file, ensure_ascii=False, indent=4)

        print("Успех! Файл questions.json успешно дополнен всеми правильными ответами.")

    except FileNotFoundError:
        print("Ошибка: Файл questions.json не найден в текущей папке. Убедитесь, что он лежит рядом со скриптом.")


if __name__ == '__main__':
    update_json_file()
